## 1. Faça um Programa que peça dois números, realize as principais operações soma, subtração, multiplicação, divisão

print("Olá! Vamos realizar algumas operações matemáticas básicas.")
numero1 = float(input("Digite o primeiro número: "))
numero2 = float(input("Digite o segundo número: "))

print("Você escolheu os números (numero1) e (numero2). Agora, vamos ver a soma, subtração, multiplicação e divisão desses números.")

soma = numero1 + numero2
subtracao = numero1 - numero2
multiplicacao = numero1 * numero2
divisao = numero1 / numero2

print(f"A soma é: {soma}")
print(f"A subtração é: {subtracao}")
print(f"A multiplicação é: {multiplicacao}")
print(f"A divisão é: {divisao}")